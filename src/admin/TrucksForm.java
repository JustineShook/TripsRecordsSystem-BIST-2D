    private void truckTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_truckTableMouseClicked
     int rowIndex = truckTable.getSelectedRow();
        
        if(rowIndex<0){
            JOptionPane.showMessageDialog(null, "Please select Item!");
        }else{
          
            
            try{
            dbConnector dbc = new dbConnector();
            TableModel tbl = truckTable.getModel();
            ResultSet rs = dbc.getData("SELECT*FROM tbl_truck WHERE truck_id = '"+tbl.getValueAt(rowIndex, 0)+"'");
            if(rs.next()){
     
                tID.setText(""+rs.getInt("truck_id"));
                PlateNumber.setText(""+rs.getString("plate_number"));
                Model.setText(""+rs.getString("model"));
                Capacity.setText(""+rs.getString("capacity"));
                jTruckStatus.setSelectedItem(""+rs.getString("truck_status"));              
                p_add.setEnabled(false);
                jLabel4.setForeground(red);
                checkadd = false;
               
    
           
            }
            }catch(SQLException ex){  
              System.out.println(""+ex);
            }
        
        }
    
    }//GEN-LAST:event_truckTableMouseClicked

