    private void p_editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_p_editMouseClicked
        if(tID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Select Trucks First! ");
        }else{
            if(PlateNumber.getText().isEmpty()||Model.getText().isEmpty()||Capacity.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "All Field are Required!");
            }else{
                dbConnector dbc = new dbConnector();
                dbc.updateData("UPDATE tbl_truck SET plate_number = '"+PlateNumber.getText()+"',"
                    + "model ='"+Model.getText()+"',"
                    + "capacity = '"+Capacity.getText()+"', "
                    + "truck_status = '"+jTruckStatus.getSelectedItem()+"' WHERE truck_id  = '"+tID.getText()+"'");
                JOptionPane.showMessageDialog(null, "Updated Successfully! ");
                displayData();
                tID.setText("");
                PlateNumber.setText("");
                Model.setText("");
                Capacity.setText("");
                jTruckStatus.setSelectedItem("");

            }

        }

    }//GEN-LAST:event_p_editMouseClicked

